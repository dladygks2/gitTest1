package ch01;

public class DataType2 {
// main + ctrl + space key + Enter

	// 메인함수 ( 코드의 시작점)
	public static void main(String[] args) {

		// 기본 자료형 - 문자 자료형

		char name; // 변수의 선언
		char a;
		char initial;
		
		
		// 값을 넣는 방법 -> 값을 초기화 한다.
		name = 'A'; 	// 홑 따옴표를 사용하고 단 하나의 글자만 변수에 저장할 수 있다.
		// 'A' : 문자, "A" : 문자열 . 의미하는 것이 다르다.
		// name= "AB";
		a = 'B';
		initial = 'C';
		// 문제 1 2 3 .화면에 변수 name, a, initial 출력해 주세요.
		
		System.out.println(name);
		System.out.println(a);
		System.out.println(initial);
		System.out.println(initial);
		// ctrl + alt + 방향키 : 그 줄을 복사한다.
		System.out.println("============");
		System.out.println('a');
		System.out.println('b');
		System.out.println('c');
		
		
		
	}// end of main

}// end of class
