package ch01;

public class DataType4 {
	
	// 메인함수 (코드의 시작점)
	public static void main(String[] args) {
		
		// 기본 자료형 - 논리 자료형(사실을 관찰하고 결과값이 참, 거짓 판별), true or false를 반환
		
		boolean isMarried = false; // 변수의 선언과 초기화를 동시에
		System.out.println(isMarried);
		// ctrl + f11 : console 출력
		isMarried = true;
		System.out.println(isMarried);
		System.out.println(0);
		
		
		
	} // end of main

} // end of class
